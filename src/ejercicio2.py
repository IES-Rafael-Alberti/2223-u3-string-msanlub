#Dado que fruta es una variable de tipo cadena, ¿qué significa fruta[:]?


fruta = "fruta"
print(fruta[:])
'''imprime todos los caracteres de fruta (fruta)'''


print(fruta[:1])
'''imprime el segundo caracter (r)'''
print(fruta[:-1])
'''imprime todo menos el último caracter(frut)'''
print(fruta[::-1])
'''imprime fruta inverso(aturf)'''
print(fruta[::-3])
'''imprime el último carácter y salta dos hasta el tecero (ar)'''